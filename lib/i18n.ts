export type Language = 'ru' | 'uk'

export function detectLanguage(): Language {
  if (typeof window === 'undefined') return 'ru'
  
  const browserLang = navigator.language.toLowerCase()
  if (browserLang.startsWith('uk')) return 'uk'
  return 'ru'
}
